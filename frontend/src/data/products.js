/**
 * Get products by brand slug.
 */
export function getProductsByBrand(brandSlug) {
  if (!brandSlug) return allProducts
  return allProducts.filter(p => brandNameToSlug[p.brand] === brandSlug)
}

/**
 * Lookup product by slug, returns product or null.
 */
export function getProductBySlug(slug) {
  return allProducts.find((p) => p.slug === slug) || null
}

/**
 * Search products by keyword (matches name, brand, category, description).
 */
export function searchProducts(keyword) {
  if (!keyword) return allProducts
  const q = keyword.toLowerCase()
  return allProducts.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      (p.category && p.category.toLowerCase().includes(q)) ||
      (p.description && p.description.toLowerCase().includes(q))
  )
}

/**
 * Get products by category slug.
 */
export function getProductsByCategory(categorySlug) {
  if (!categorySlug) return allProducts
  return allProducts.filter((p) => p.category === categorySlug)
}
